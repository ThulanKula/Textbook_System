package com.thulani.repository.impl;

import com.thulani.entity.Department;
import com.thulani.factory.DepartmentFactory;
import com.thulani.repository.DepartmentRepository;
import org.junit.Test;
import org.junit.Assert;

public class DepartmentRepositoryImplTest {

    private static DepartmentRepository departmentRepository = DepartmentRepositoryImpl.getDepartmentRepository();
    private static Department department = DepartmentFactory.createDepartment("110", "Information Technology");

    @Test
    public void getDepartmentRepository() {
    }

    @Test
    public void getAll() {
        System.out.println("Get All: " + departmentRepository.getAll());
    }

    @Test
    public void create() {
        Department create = departmentRepository.create(department);
        Assert.assertEquals(department.getDepartmentNum(), create.getDepartmentNum());
        System.out.println("Department Number: " + department);
    }

    @Test
    public void read() {
        Department read = departmentRepository.read(department.getDepartmentNum());
        System.out.println("Read: " + read);
    }

    @Test
    public void update() {
        Department update = new Department.Builder().setDepartmentNum("123").setDepartmentName("Chemical Engineering").build();
        update = departmentRepository.update(update);
        System.out.println("Update: " + update);
    }

    @Test
    public void delete() {
        boolean delete = departmentRepository.delete(department.getDepartmentNum());
        Assert.assertTrue(delete);
    }
}
